let a = parseInt (prompt ("Select number of a day"));

switch (a) {
    case 1: 
    alert("Its Monday")
    break;

    case 2: 
    alert("Its Tuesday")
    break;

    case 3: 
    alert("Its Wednesday")
    break;

    case 4: 
    alert("Its Thursday")
    break;

    case 5: 
    alert("Its Friday")
    break;

    case 6: 
    alert("Its Saturday")
    break;
    
    case 7: 
    alert("Its Sunday")
    break;

    default:
    alert ("Invalid number")
    
}